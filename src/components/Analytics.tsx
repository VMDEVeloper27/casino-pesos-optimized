import Script from 'next/script';

// Declaración global para TypeScript
declare global {
  interface Window {
    gtag: (...args: any[]) => void;
    dataLayer: any[];
  }
}

// Helper functions para tracking de eventos
export const trackEvent = (
  action: string,
  category: string,
  label?: string,
  value?: number
) => {
  if (typeof window !== 'undefined' && window.gtag) {
    window.gtag('event', action, {
      event_category: category,
      event_label: label,
      value: value,
    });
  }
};

// Eventos específicos para casinos
export const trackCasinoClick = (casinoName: string, position?: number) => {
  trackEvent('casino_click', 'Casino', casinoName, position);
};

export const trackBonusClaim = (bonusType: string, casinoName: string) => {
  trackEvent('bonus_claim', 'Bonus', `${bonusType} - ${casinoName}`);
};

export const trackGamePlay = (gameName: string, gameType: string) => {
  trackEvent('game_play', 'Game', `${gameType}: ${gameName}`);
};

export const trackSignup = (method: string) => {
  trackEvent('sign_up', 'User', method);
};

export const trackSearch = (searchTerm: string) => {
  trackEvent('search', 'Site', searchTerm);
};

interface GoogleAnalyticsProps {
  measurementId: string;
}

export function GoogleAnalytics({ measurementId }: GoogleAnalyticsProps) {
  return (
    <>
      <Script
        src={`https://www.googletagmanager.com/gtag/js?id=${measurementId}`}
        strategy="afterInteractive"
      />
      <Script id="google-analytics" strategy="afterInteractive">
        {`
          window.dataLayer = window.dataLayer || [];
          function gtag(){dataLayer.push(arguments);}
          gtag('js', new Date());
          gtag('config', '${measurementId}', {
            page_path: window.location.pathname,
          });
        `}
      </Script>
    </>
  );
}

